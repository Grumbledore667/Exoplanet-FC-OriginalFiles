local function getLabel( objectName )
   if ( not objectName ) then
      return ""
   end

   local info = ObjectsLabels[objectName]

   if ( info ) then
      return info.label.eng
   end

   return ""
end

local function getInteractLabel( objectName )
   if ( not objectName ) then
      return ""
   end

   local info = ObjectsLabels[objectName]

   if ( info ) then
      return info.interactLabel.eng
   end

   return ""
end

